    /**
     * Busca as edições
     */
    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // remove os itens
        this.jComboBox2.removeAllItems();
        // pega o nome do evento selecionado
        String event_name = (String) this.jComboBox1.getSelectedItem();
        try {
            // pega as edições do evento
            ResultSet rs = Edition.findByEvent(MainFrame.db, event_name);
            Edition ed = Edition.next(rs);
            while(ed != null) {
                String info = ed.getNumEd() + " de " + ed.getDataInicioEd() + " a " + ed.getDataFimEd()
                        + " em " + ed.getLocalEd();
                this.jComboBox2.addItem(info);
                ed = Edition.next(rs);
            }   
            // fecha o cursor
            MainFrame.db.close();
        } catch(SQLException e) {
            Message msg = new Message(this, true, e.getMessage());
            msg.setTitle("Erro");
            msg.setVisible(true);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    /**
     * Busca os patrocínios
     */
    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        // remove os itens
        this.jComboBox3.removeAllItems();
        try {
            // pega o evento selecionado
            String eventName = (String) this.jComboBox1.getSelectedItem();
            ResultSet rs = Event.findByName(MainFrame.db, eventName);
            Event e = Event.next(rs);
            int codEv = e.getCodEv();
            // fecha o cursor
            MainFrame.db.close();
            
            // pega a edição selecionada
            String editionName = (String) this.jComboBox2.getSelectedItem();
            if (editionName == null)
                return;
            String[] parts = editionName.split(" ");
            int numEd = Integer.valueOf(parts[0]).intValue();
            
            // busca os patrocínios da edição do evento
            this.cnpjs = new ArrayList();
            rs = Sponsorship.findByEventAndEdition(MainFrame.db, codEv, numEd, true);
            while(rs.next()) {
                // adiciona os cnpjs dos patrocinadores no array
                this.cnpjs.add(rs.getString("cnpjPat"));
                // adiciona o nome do inscrito na ComboBox
                this.jComboBox3.addItem(rs.getString("razaoSocialPat"));
            }
            // fecha o cursor
            MainFrame.db.close();
        
        } catch(SQLException e) {
            Message msg = new Message(this, true, e.getMessage());
            msg.setTitle("Erro");
            msg.setVisible(true);
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

    /**
     * Cadastrar despesa
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // pega os itens selecionados
        String eventName = (String) this.jComboBox1.getSelectedItem();
        String editionName = (String) this.jComboBox2.getSelectedItem();
        int sponsor_index = this.jComboBox3.getSelectedIndex();
        
        // campos obrigatórios não preenchidos
        if (eventName == null || editionName == null || sponsor_index == -1) {
            Message msg = new Message(this, true, "Campos obrigatórios não preenchidos.");
            msg.setTitle("Erro");
            msg.setVisible(true);
            return;
        }
        
        String descricaoDesp = null;
        if (!this.jTextArea1.getText().isEmpty())
            descricaoDesp = this.jTextArea1.getText();
        
        String dataDesp = null;
        if (!this.jFormattedTextField1.getText().equals("  /  /    "))
            dataDesp = this.jFormattedTextField1.getText();
        
        // valor da despesa
        double valorDesp = 0.00;
        if (!this.jFormattedTextField2.getText().isEmpty()) {
            String aux = this.jFormattedTextField2.getText().replace(',', '.');
            valorDesp = Double.valueOf(aux).doubleValue();
        }
        
        try {
            // pega o evento selecionado
            ResultSet rs = Event.findByName(MainFrame.db, eventName);
            Event ev = Event.next(rs);
            int codEv = ev.getCodEv();
            // fecha o cursor
            MainFrame.db.close();
            
            // pega a edição selecionada
            String[] parts = editionName.split(" ");
            int numEd = Integer.valueOf(parts[0]).intValue();
            
            // pega o patrocinador selecionado
            Sponsorship ss = Sponsorship.findByPrimaryKey(MainFrame.db, this.cnpjs.get(sponsor_index), codEv, numEd);
            
            // insere a despesa
            Expense e = new Expense(codEv, numEd, ss.getCnpjPat(), ss.getCodEv(), ss.getNumEd(), dataDesp, valorDesp, 
                    descricaoDesp);
            e.insert(MainFrame.db);
            new Message(this, true, "Despesa cadastrada.").setVisible(true);
            this.dispose();
            
        } catch(SQLException e) {
            String error;
            if (e.getErrorCode() == 12899)
                error = "Tamanho de atributo excedido.";
            else if (e.getErrorCode() == 1843 || e.getErrorCode() == 1847)
                error = "Data inválida";
            else
                error = e.getMessage();
            Message msg = new Message(this, true, error);
            msg.setTitle("Erro");
            msg.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

