    /**
     * Inicia a tela de atualização ou remoção de patrocínio
     */
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (this.type != CRUDType.SEARCH) {
            // pega o patrocínio selecionado
            int i = this.jTable1.getSelectedRow();
            try {
                // pega o código do evento
                ResultSet rs = Event.findByName(MainFrame.db, (String) this.jTable1.getValueAt(i, 1));
                Event ev = Event.next(rs);
                // fecha o cursor
                MainFrame.db.close();

                String aux;
                StringBuffer tmp = new StringBuffer();
                
                aux = (String) this.jTable1.getValueAt(i, 3);
                for(int j = 0; j < aux.length(); j++) {
                    if (aux.charAt(j) >= '0' && aux.charAt(j) <= '9')
                        tmp.append(aux.charAt(j));
                    if(aux.charAt(j) == ',')
                        tmp.append(".");
                }
                double valorPat = Double.parseDouble(tmp.toString());
                
                tmp = new StringBuffer();
                aux = (String) this.jTable1.getValueAt(i, 4);
                for(int j = 0; j < aux.length(); j++) {
                    if (aux.charAt(j) >= '0' && aux.charAt(j) <= '9')
                        tmp.append(aux.charAt(j));
                    if(aux.charAt(j) == ',')
                        tmp.append(".");
                }
                double saldoPat = Double.parseDouble(tmp.toString());
                
                Sponsorship ss = new Sponsorship(this.cnpjs.get(i), ev.getCodEv(), (int) this.jTable1.getValueAt(i, 2), 
                    valorPat, saldoPat, (String) this.jTable1.getValueAt(i, 5));
                
                // inicia a interface de atualização
                if (this.type == CRUDType.UPDATE) {
                    UpdateSponsorship updateSponsorship = new UpdateSponsorship(ss);
                    updateSponsorship.setVisible(true);
                    this.dispose();
                }
                // inicia a interface de remoção
                if (this.type == CRUDType.REMOVE) {
                    RemoveSponsorship removeSponsorship = new RemoveSponsorship(ss);
                    removeSponsorship.setVisible(true);
                    this.dispose();
                }
                
            } catch(SQLException e) {
                Message msg = new Message(this, true, e.getMessage());
                msg.setTitle("Erro");
                msg.setVisible(true);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

