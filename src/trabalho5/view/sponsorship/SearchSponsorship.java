    public SearchSponsorship(int type, String name) {
        this.type = type;
        this.cnpjs = new ArrayList();
        initComponents();
        
        // imprime os patrocínios
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        try {
            ResultSet rs;
            // SELECT ALL
            if (name.isEmpty())
                rs = Sponsorship.findViewAll(MainFrame.db);
            // SELECT By Name
            else
                rs = Sponsorship.findViewByName(MainFrame.db, name);
            
            NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
            
            while(rs.next()) {
                // armazena os cnpjs dos patrocinadores
                this.cnpjs.add(rs.getString("cnpjPat"));
                // adiciona uma linha na tabela
                model.addRow(new Object[] {rs.getString("razaoSocialPat"), rs.getString("nomeEv"), rs.getInt("numEd"), 
                    nf.format(rs.getDouble("valorPat")), nf.format(rs.getDouble("saldoPat")), rs.getString("dataPat")});
            }
            // fecha o cursor
            MainFrame.db.close();
            
        } catch(SQLException e) {
            Message msg = new Message(this, true, e.getMessage());
            msg.setTitle("Erro");
            msg.setVisible(true);
        }
    }

