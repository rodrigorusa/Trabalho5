    /**
     * Cadastrar
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // pega os valores
        String aux = this.jFormattedTextField2.getText();
        String parts[] = aux.split("[./-]");
        
        String cnpjPat = parts[0];
        for(int i = 1; i < parts.length; i++)
            cnpjPat += parts[i];
        
        String razaoSocialPat = this.jTextField2.getText();
        
        // campos obrigatórios não preeenchidos
        if (aux.equals("  .   .   /    -  ") || razaoSocialPat.isEmpty()) {
            Message msg = new Message(this, true, "Campos obrigatórios não preenchidos.");
            msg.setTitle("Erro");
            msg.setVisible(true);
            return;
        }
        
        String enderecoPat = null;
        if (!this.jTextField4.getText().isEmpty())
            enderecoPat = this.jTextField4.getText();
        
        String telefonePat = null;
        if (!this.jFormattedTextField1.getText().equals("(  )     -    "))
            telefonePat = this.jFormattedTextField1.getText();
        
        Sponsor sponsor = new Sponsor(cnpjPat, razaoSocialPat, telefonePat, enderecoPat);
        
        try {
            sponsor.insert(MainFrame.db);
            new Message(this, true, "Patrocinador cadastrado.").setVisible(true);
            this.dispose();
        } catch(SQLException e) {
            String error;
            if (e.getErrorCode() == 1)
                    error = "Patrocinador já cadastrada.";
            else if (e.getErrorCode() == 12899)
                    error = "Tamanho de atributo excedido.";
            else
                    error = e.getMessage();
            Message msg = new Message(this, true, error);
            msg.setTitle("Erro");
            msg.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

