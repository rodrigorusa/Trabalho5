    public SearchEvent(int type, String name) {
        this.type = type;
        initComponents();
        
        // Imprime todos os eventos
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        try {
            ResultSet rs;
            // SELECT ALL
            if (name.isEmpty())
                rs = Event.findAll(MainFrame.db);
            // SELECT BY NAME
            else
                rs = Event.findByName(MainFrame.db, name);
            Event e = Event.next(rs);
            while (e != null) {
                // adiciona uma linha na tabela
                model.addRow(new Object[]{e.getCodEv(), e.getNomeEv(), e.getDescricaoEv(), e.getWebsiteEv(), 
                    e.getTotalArtigosApresentadosEv()});
                e = Event.next(rs);
            }
            // fecha o cursor
            MainFrame.db.close();
        } catch(SQLException e) {
            Message msg = new Message(this, true, e.getMessage());
            msg.setTitle("Erro");
            msg.setVisible(true);
        }
    }

