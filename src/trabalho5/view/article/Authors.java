    public Authors(Article a) {
        this.article = a;
        initComponents();
        
        // imprime os autores
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        try {
            ResultSet rs = Write.findViewByArticle(MainFrame.db, this.article.getIdArt());
            while(rs.next()) {
                // adiciona uma linha na tabela
                model.addRow(new Object[] {rs.getString("nomePe"), rs.getString("emailPe"), rs.getString("instituicaoPe"),
                    rs.getString("telefonePe")});
            }
            // fecha o cursor
            MainFrame.db.close();
        } catch(SQLException e) {
            Message msg = new Message(this, true, e.getMessage());
            msg.setTitle("Erro");
            msg.setVisible(true);
        }
    }

