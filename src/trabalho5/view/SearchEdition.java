    public SearchEdition(int type, String option) {
        this.type = type;
        initComponents();
        
        // Imprime todos os eventos
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        try {
            ResultSet rs;
            // SELECT ALL
            if (option.equals("Todos"))
                rs = Edition.findAll(MainFrame.db);
            // SELECT BY Event
            else {
                // pega o evento selecionado
                rs = Event.findByName(MainFrame.db, option);
                Event ev = Event.next(rs);
                // fecha o cursor
                MainFrame.db.close();
                rs = Edition.findByEvent(MainFrame.db, ev);
            }    
            Edition ed = Edition.next(rs);
            while (ed != null) {
                // adiciona uma linha na tabela
                model.addRow(new Object[]{ed.getCodEv(), ed.getNumEd(), ed.getDescricaoEd(), ed.getDataInicioEd(), 
                    ed.getDataFimEd(), ed.getLocalEd(), ed.getTaxaEd(), ed.getSaldoFinanceiroEd(), 
                    ed.getQtdArtigosApresentadosEd()});
                ed = Edition.next(rs);
            }
            // fecha o cursor
            MainFrame.db.close();
        } catch(SQLException e) {
            Message msg = new Message(this, true, e.getMessage());
            msg.setTitle("Erro");
            msg.setVisible(true);
        }
    }

